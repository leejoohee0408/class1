	// 연산자를 확인하여 연산 수행
	 double compute(double i, String s, double ii) {
		 
        if (s.equals("+")) {
            return i + ii;
        } else if (s.equals("-")) {
            return i - ii;
        } else if (s.equals("*")) {
            return i * ii;
        } else if (s.equals("/")) {
            	return i / ii;  
            }
        
        return i / ii;
	} 
        
        
        
        
        
    
	
	
	
	
	
		
	
	
	
	
	
	
	
